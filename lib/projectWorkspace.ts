export function getProjectStages(): ProjectStage[] {
  return getCurriculum().modules.map((module, index) => {
    const [title, explanation, prompt] = STAGE_GUIDANCE[index];
    return {
      id: module.id,
      number: index + 1,
      title,
      explanation: `${explanation} הפרויקט של מודול זה: ${module.project?.title ?? module.title}.`,
      prompt,
    };
  });
}
